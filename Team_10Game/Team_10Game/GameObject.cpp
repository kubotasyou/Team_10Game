ID3D12Device* GameObject::device = nullptr;
UINT GameObject::descriptorHandleIncrementSize = 0;
ID3D12GraphicsCommandList* GameObject::cmdList = nullptr;
ComPtr<ID3D12RootSignature> GameObject::rootsignature;
ComPtr<ID3D12PipelineState> GameObject::pipelinestate;
ComPtr<ID3D12DescriptorHeap> GameObject::descHeap;
XMMATRIX GameObject::matView{};
XMMATRIX GameObject::matProjection{};
XMFLOAT3 GameObject::eye = { 0, 5, -50.0f };
XMFLOAT3 GameObject::target = { 0, 0, 0 };
XMFLOAT3 GameObject::up = { 0, 1, 0 };

void GameObject::StaticInitialize(ID3D12Device * device, int window_width, int window_height)
{
	// nullptrチェック
	if (device == nullptr)
	{
		assert(0 + "デバイスが読み込めていません");
	}

	GameObject::device = device;

	// デスクリプタヒープの初期化
	InitializeDescriptorHeap();

	// カメラ初期化
	InitializeCamera(window_width, window_height);

	// パイプライン初期化
	InitializeGraphicsPipeline();
}

void GameObject::BeginDraw(ID3D12GraphicsCommandList * cmdList)
{
	// PreDrawとPostDrawがペアで呼ばれていなければエラー
	if (GameObject::cmdList == nullptr)
	{
		assert("コマンドリストが入っていません");
	}

	// コマンドリストをセット
	GameObject::cmdList = cmdList;

	// パイプラインステートの設定
	cmdList->SetPipelineState(pipelinestate.Get());
	// ルートシグネチャの設定
	cmdList->SetGraphicsRootSignature(rootsignature.Get());
	// プリミティブ形状を設定
	cmdList->IASetPrimitiveTopology(D3D_PRIMITIVE_TOPOLOGY_TRIANGLELIST);
}

void GameObject::EndDraw()
{
	// コマンドリストを解除
	GameObject::cmdList = nullptr;
}

